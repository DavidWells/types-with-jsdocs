/**
 * Adds two numbers
 * 
 * @param {number} a number one
 * @param {number} b number two
 * @returns {number} sum of number one and number two
 * 
 * @example <caption>Add 1 and 3. If answer is >0, sums result and 5.</caption>
 * ```
 * const answer = sum(1, 3); // returns 4
 * 
 * if (answer > 0) {
 *   sum(answer, 5)
 * }
 * ```
 */
function sum(a, b) {
  return a + b
}
